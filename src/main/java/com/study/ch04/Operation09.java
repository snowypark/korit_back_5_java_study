package com.study.ch04;

public class Operation09 {
    public static void main(String[] args) {

        /*
            << 증감연산자 >>
            ++i 선 증가
            i++ 후 증가
         */

        int i = 0;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);

    }
}
